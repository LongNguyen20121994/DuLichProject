package model.bean;

public class InfoNganhDHCD {
	private String maNganh;
	private String tenNganh;
	private String daoTao;
	private String maKhoi;
	private int chiTieu;
	private String ghiChu;

	public String getMaNganh() {
		return maNganh;
	}

	public void setMaNganh(String maNganh) {
		this.maNganh = maNganh;
	}

	public String getTenNganh() {
		return tenNganh;
	}

	public void setTenNganh(String tenNganh) {
		this.tenNganh = tenNganh;
	}

	public String getDaoTao() {
		return daoTao;
	}

	public void setDaoTao(String daoTao) {
		this.daoTao = daoTao;
	}

	public String getMaKhoi() {
		return maKhoi;
	}

	public void setMaKhoi(String maKhoi) {
		this.maKhoi = maKhoi;
	}

	public int getChiTieu() {
		return chiTieu;
	}

	public void setChiTieu(int chiTieu) {
		this.chiTieu = chiTieu;
	}

	public String getGhiChu() {
		return ghiChu;
	}

	public void setGhiChu(String ghiChu) {
		this.ghiChu = ghiChu;
	}

}
