package model.bean;

public class ChiTietHoSo {
	private String maHoSo;
	private String maKhoi;
	private String maTruong;
	private String maNganh;
	private String daoTao;
	private String tongDiem;

	public String getMaHoSo() {
		return maHoSo;
	}

	public void setMaHoSo(String maHoSo) {
		this.maHoSo = maHoSo;
	}

	public String getMaKhoi() {
		return maKhoi;
	}

	public void setMaKhoi(String maKhoi) {
		this.maKhoi = maKhoi;
	}

	public String getMaTruong() {
		return maTruong;
	}

	public void setMaTruong(String maTruong) {
		this.maTruong = maTruong;
	}

	public String getMaNganh() {
		return maNganh;
	}

	public void setMaNganh(String maNganh) {
		this.maNganh = maNganh;
	}

	public String getDaoTao() {
		return daoTao;
	}

	public void setDaoTao(String daoTao) {
		this.daoTao = daoTao;
	}

	public String getTongDiem() {
		return tongDiem;
	}

	public void setTongDiem(String tongDiem) {
		this.tongDiem = tongDiem;
	}

}
