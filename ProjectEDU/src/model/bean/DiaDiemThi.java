package model.bean;

public class DiaDiemThi {
	private String maDDT;
	private String maHDT;
	private String tenDDT;
	private String diaChi;

	public String getMaDDT() {
		return maDDT;
	}

	public void setMaDDT(String maDDT) {
		this.maDDT = maDDT;
	}

	public String getMaHDT() {
		return maHDT;
	}

	public void setMaHDT(String maHDT) {
		this.maHDT = maHDT;
	}

	public String getTenDDT() {
		return tenDDT;
	}

	public void setTenDDT(String tenDDT) {
		this.tenDDT = tenDDT;
	}

	public String getDiaChi() {
		return diaChi;
	}

	public void setDiaChi(String diaChi) {
		this.diaChi = diaChi;
	}

}
